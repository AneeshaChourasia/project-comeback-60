// JavaScript Lesson 04 - Comparison Operators

// Equal To (==)

let num1 = 10;
let num2 = 10;

console.log(num1 == num2);

// Strict Equal (===)

let num3 = 10;
let num4 = "10";

console.log(num3 === num4);

// Not Equal (!=)

let num5 = 20;
let num6 = 15;

console.log(num5 != num6);

// Strict Not Equal (!==)

let num7 = 25;
let num8 = "25";

console.log(num7 !== num8);

// Greater Than (>)

let age = 20;

console.log(age > 18);

// Less Than (<)

let marks = 35;

console.log(marks < 50);

// Greater Than or Equal (>=)

let score = 80;

console.log(score >= 80);

// Less Than or Equal (<=)

let passMarks = 33;

console.log(passMarks <= 35);