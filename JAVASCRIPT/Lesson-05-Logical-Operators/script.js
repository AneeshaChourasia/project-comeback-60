// JavaScript Lesson 05 - Logical AND (&&)

// Example 1

let age = 20;
let marks = 80;

console.log(age >= 18 && marks >= 60);

// Example 2

let salary = 25000;
let experience = 3;

console.log(salary >= 30000 && experience >= 2);

// Example 3

let isStudent = true;
let hasIDCard = true;

console.log(isStudent && hasIDCard);

// Logical OR (||)

// Example 1

let num = 20;
let specialPermission = true;

console.log(num >= 18 || specialPermission);

// Example 2

let isMember = false;
let hasCoupon = false;

console.log(isMember || hasCoupon);

// Topic: Logical NOT (!)

let isLoggedIn = true;

console.log(!isLoggedIn);
