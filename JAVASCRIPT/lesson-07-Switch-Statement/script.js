// Switch Statement

let day = 2;

switch (day) {
  case 1:
    console.log("Monday");
    break;

  case 2:
    console.log("Tuesday");
    break;

  case 3:
    console.log("wednesday");
    break;

  default:
    console.log("Invalid Day");
}

let fruit = "Apple";

switch (fruit) {
  case "Apple":
    console.log("Red Fruit");
    break;

  case "Banana":
    console.log("Yellow Fruit");
    break;

  case "Mango":
    console.log("King of Fruit");
    break;

  default:
    console.log("Unknown Fruit");
}

let role = "Admin";

switch (role) {
  case "Teacher":
    console.log("Welcome Teacher");
    break;

  case "Admin":
    console.log("Welcome Admin");
    break;

  case "Student":
    console.log("Welcome Student");
    break;

  default:
    console.log("Invalid Role");
}
